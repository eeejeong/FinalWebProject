package com.medirone.web.dto;

public class Manager {
	private String manager_id;
	private String manager_name;
	private String manager_tel;
	private String manager_email;
	private String manager_agency_id;
	
	public String getManager_id() {
		return manager_id;
	}
	public void setManager_id(String manager_id) {
		this.manager_id = manager_id;
	}
	public String getManager_name() {
		return manager_name;
	}
	public void setManager_name(String manager_name) {
		this.manager_name = manager_name;
	}
	public String getManager_tel() {
		return manager_tel;
	}
	public void setManager_tel(String manager_tel) {
		this.manager_tel = manager_tel;
	}
	public String getManager_email() {
		return manager_email;
	}
	public void setManager_email(String manager_email) {
		this.manager_email = manager_email;
	}
	public String getManager_agency_id() {
		return manager_agency_id;
	}
	public void setManager_agency_id(String manager_agency_id) {
		this.manager_agency_id = manager_agency_id;
	}
}
